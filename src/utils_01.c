#include "corewar.h"

UI	convert_to_ui(char *byte)
{
	UI	n;

	n = 0;
	n |= (UI)byte[0] << 24;
	n |= (UI)byte[1] << 16;
	n |= (UI)byte[2] << 8;
	n |= (UI)byte[3];
	return (n);
}
