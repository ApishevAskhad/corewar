#include "corewar.h"

UI	convert_to_ui(UC byte[4])
{
	UI	n;
	UI	*ptr;

	n = 0;
	ptr = (UI*)byte;
	n |= (*ptr & 0xFF000000) >> 24;
	n |= (*ptr & 0x00FF0000) >> 8;
	n |= (*ptr & 0x0000FF00) << 8;
	n |= (*ptr & 0x000000FF) << 24;
	return (n);
}
